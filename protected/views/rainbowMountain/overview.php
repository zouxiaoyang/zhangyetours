    <h3>Rainbow Mountain China Overview</h3>
    <h5>Geo Location</h5>
    <p>Zhangye Danxia is also known as the Rainbow Mountain with an area of 529 square kilometers, of which the color hilly area is about 50 square kilometers. It is located in Linze County and is 40 kilometers from the city. The altitude is between 2000 meters and 3800 meters. It is 40 kilometers from east to west and 5 to 10 kilometers from north to south. Thousands of cliff mountains present bright red and reddish brown which against each other showing its special features.</p>
    <h5>How to Form It?</h5>
    <p>The geological structure of Zhangye Danxia is the formation of rock accumulation which means the red sandstone develops isolated peaks and steep rocks through the weathering and water’s erosion for a long time along with the special geological structure and climate change. It is mainly composed of red gravel, sandstone and mudstone. </p>
    <h5>The Award</h5>
    <p>Zhangye Danxia landform is China’s only Danxia landform in high recombination with color hilly landscape. It is odd and special for hierarchical scattered levels, steep cliffs, rich forms, amazing colors, with a high ornamental value and important scientific value. It enjoys the honors of “China’s top seven most beautiful Danxia landforms”, “China’s top seven most beautiful exotic landforms” and the “the world’s top ten magical geographical wonders”, and is a best place for tourism, painting, photography and science education.</p>
	 <h5>Guide for Travel</h5>
    <p>The best time to visit the Coloured Mountains China is June to September for the reason that the humid air and cool weather will make you feel much more comfortable.
	The price of the entrance ticket is 40 Yuan (It may rise to 80 Yuan since 2016). And the price for the sightseeing bus is 20 Yuan.
	The time for ticket selling in high season is 06:00-18:00 (The closing time is around 21:00 in the evening.). The selling time in low season is: 08:00-17:30.
	There are two entrances in the park: entrance in Linze County (the west entrance) and entrance in Sunan County (the east entrance). We suggest you to go from the west entrance as it is more convenient for accommodation and transportation.
	</p>
    
