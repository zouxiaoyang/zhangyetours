    <h3>Best Time to Visit</h3>
	<p>June to September of each year is Zhangye’s summer with cool weather and rich water. It is not cold to watch the sunrise or sunset in Zhangye, so summer and fall are the best season to travel the Rainbow Mountain China. While in winter, the weather becomes windy, cold and dry. Thus it is not suitable for sightseeing.</p>

	<h5>Best Weather to Visit</h5>
	<p>The sunny days after the rain will make the color of Danxia brighter and it is the best weather to visit Rainbow Mountain China. Besides, it is also a good choice to watch the sunrise or sunset in sunny days as the sunshine will make Danxia present different layers. There is almost nothing can be seen in cloudy days because Danxia’s most beautiful color cannot be presented.</p>

    <p><span style="font-weight:bold;">1.	Watch the Sunrise of Rainbow Mountain</span><br>The Zhangye Danxia Geopark opens at 6:00am in the morning.
	The best spots to watch the sunrise: The fourth and the fifth viewing platforms.
	The highest spot to watch: The second viewing platform. It is very high and you need to climb to the top for about 20 minutes but you can enjoy the full view of the Colored Mountain China.
	Kindly reminder: It is a little cold in the morning in Zhangye Danxia Landform and you’d better wear a thick coat. But it would be warmer after the sun rises.
	</p>
   
    <p> <span style="font-weight:bold;">2.	Watch the Sunset of Rainbow Mountain </span><br>The best platform to watch the sunset: The first, fourth and fifth viewing platforms. Actually at the fourth and fifth viewing platform, you can see “a mountain of swords and a sea of flames”. But the fourth viewing platform is further and higher with a full view of Danxia Geopark; while the fifth platform is closer to the spot “a mountain of swords and a sea of flames”. So it depends on you to choose the full view or close-up.
	Sunset time of Zhangye Danxia Landform Geological Park in the whole year:<br />
	Sunset time in Jan. about 17:30-18:00<br />
	Sunset time in Feb. about 18:00-18:30<br />
	Sunset time in Mar. about 18:30-19:00<br />
	Sunset time in Apr. about 19:00-19:30<br />
	Sunset time in May. about 19:30-20:00<br />
	Sunset time in Jun. about 20:00-20:30<br />
	Sunset time in Jul. about 20:15-20:30<br />
	Sunset time in Aug. about 20:00-20:30<br />
	Sunset time in Sep. about 19:00-19:30<br />
	Sunset time in Oct. about 18:00-18:30<br />
	Sunset time in Nov. about 17:30-18:00<br />
	Sunset time in Dec. about 17:00-17:30<br />
	Note: The itinerary is used for the whole year. Watch the sunset in the sunset time depends on the specific time.
	</p>
	 
	<p><span style="font-weight:bold;">3.	Watch the sunrise and sunset</span><br>If you plan to watch the sunrise and sunset, you can live near the scenic zone that night. There are several three-star hotels there for you to choose. We suggest you telling your intension to the staff in the scenic zone when buying the ticket in the previous day. The staff will register your passport information and give you a receipt. You need to take the receipt to the boss of the hotel you will live in to stamp. The next morning before eight, you can go into the park once again. If you reserve a private itinerary with us, the guide can help you to arrange these things.</p>
