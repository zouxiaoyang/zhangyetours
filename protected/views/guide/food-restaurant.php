                <img src="/images/index/food_SaoziNoodles.jpg" alt="" class="img-responsive"   />
                <span>Saozi Noodles are the most common and the most popular breakfast in Zhangye. The making of noodles stresses “thin, bright, and refined,” indicating that the noodles must be thin, the color must be bright and the noodles must taste great. The chicken soup is for the best taste.—Liangmao Restaurant</span>
                </div>                
