        	<h3>Food & Restaurant</h3>
            <p>The main food in Zhangye is confined to flour food, such as Chao Pao, Saozi Noodles, Small Rice, Fish-shaped Powder, Entwisting Noodles, Sheet Jelly and Gluten, etc. And there are three food courts in Zhangye: Ming and Qing Food Street, Ganzhou Market and European Street.
            </p>
            <p>If you choose our tour packages, we will arrange these famous dishes and snacks in your itinerary. The breakfast is generally some Chinese and western breakfast buffet and you can have it in the hotel of Zhangye. For lunch and dinner, you will have them in some beautiful and clean restaurants (not the tourist restaurants). Our guide can help you order the food and recommend the appropriate restaurants for you.
            </p>            
            <div class="row food_block01">
            	<div class="col-md-4 col-sm-4 col-xs-12 food_miniblock01">
            	<label>Chao Pao</label>
                <img src="/images/index/food_ChaoPao.jpg" alt="" class="img-responsive"   />
                <span>Chao Pao is a kind of flour food. It can be long or short and tastes fine with braised pork, tomatoes and onions mixed together. You can also add some condiments like peppers or sauce to promote its flavor as you like. Also, a bowl of soup is a must as the local people like to do this.—Sunji Chao Pao</span>
                </div>
                <div class="col-md-4 col-sm-4 col-xs-12 food_miniblock01">
            	<label>Chicken Stewed Rolls</label>
                <img src="/images/index/food_ChickenStewedRolls.jpg" alt="" class="img-responsive"   />
                <span>Chicken Stewed Pancake is the blend of northwest pasta and cuisine with special taste. It is put in a big plate just like the Saute Spicy Chicken, quite enough to be shared by four people.—Miao Chicken Stewed Rolls</span>
                </div>
                <div class="col-md-4 col-sm-4 col-xs-12 food_miniblock01">
            	<label>Entwisting Noodles</label>
                <img src="/images/index/food_EntwistingNoodles.jpg" alt="" class="img-responsive"   />
                <span>This snap gets its name because it is hand-made and resembles the shape of small fish. However, just because of the hand-making procedure, its taste is smooth and clear. It can be eaten with soup or with nothing else. The original resource can be buckwheat, barley, or wheat. The eaters decide.—Wuji Entwisting Noodles</span>
                </div>
                <div class="col-md-4 col-sm-4 col-xs-12 food_miniblock01">
            	<label>Small Rice</label>
                <img src="/images/index/food_SmallRice.jpg" alt="" class="img-responsive"   />
                <span>Small Rice has its name because of the small wheat dough, small tofu and small pieces of vegetables. It is cooked by small condiments, using the local products such as red bean, sheet jelly and beef.—Yuanji Small Rice</span>
                </div>
                <div class="col-md-4 col-sm-4 col-xs-12 food_miniblock01">
            	<label>Pickled Pork</label>
                <img src="/images/index/food_PickledPork.jpg" alt="" class="img-responsive"   />
                <span>Pickled pork has been cooked in soup for a long time, tasting delicious with great scent, the fat and thin parts of pork mixing together. When the pork is out of the pan, the both ends of the pork will be a little curly. The pork could both be the appetizer and go with the wine. It is one of the indispensable snaps in the getting-together parties in the spare time of the local people.</span>
                </div>
                <div class="col-md-4 col-sm-4 col-xs-12 food_miniblock01">
            	<label>Fragrant Rice</label>
                <img src="/images/index/food_FragrantRice.jpg" alt="" class="img-responsive"   />
                <span>Fragrant Rice is also named Northwest Big Dish. When making this rice, the heart, liver, lung of pig will be minced and be added with flour and condiments such as salt, onion and garlic. Then thin pieces of fat meat will also be put into suitable amount of soup. This dish is colorful, delicious, being one of the “musts” in the dinners in Zhangye</span>
                </div>
                <div class="col-md-4 col-sm-4 col-xs-12 food_miniblock01">
            	<label>Oil Cake</label>
                <img src="/images/index/food_OilCake.jpg" alt="" class="img-responsive"   />
                <span>The surfaces of the oil cake is crisp and bright with golden color. When it is put into the mouth, it tastes soft and sweet. The color, flavor and taste of the cake are all great, and every element of making the cake from the selection of the resources, the arrangement of ingredients, the control of fire, to the production is very special.</span>
                </div>
                <div class="col-md-4 col-sm-4 col-xs-12 food_miniblock01">
            	<label>Fish-shaped Powder</label>
                <img src="/images/index/food_FishshapedPowder.jpg" alt="" class="img-responsive"   />
                <span>This snap is made by exquisite horsebean starch and looks like a small fish. The color is white or red. Eating together with condiment, you will feel cool and smooth, fulfilling both your hunger and thirst. It is a very native local snap.</span>
                </div>
                <div class="col-md-4 col-sm-4 col-xs-12 food_miniblock01">
            	<label>Sheet Jelly and Gluten</label>
                <img src="/images/index/food_SheetJellyandGluten.jpg" alt="" class="img-responsive"   />
                <span>The two snaps are both very common and very special in Zhangye. They are easy to store for a long time and easy to eat. They can be cooked with vegetables and also can be made into soups; they can even been found in chaffy dish. They are not only the main resources for famous snaps in Zhangye, but also the common ingredients in daily life.</span>
                </div>
